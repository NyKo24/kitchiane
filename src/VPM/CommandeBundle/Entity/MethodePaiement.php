<?php

namespace VPM\CommandeBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * MethodePaiement
 *
 * @ORM\Table(name="methode_paiement")
 * @ORM\Entity(repositoryClass="VPM\CommandeBundle\Repository\MethodePaiementRepository")
 */
class MethodePaiement
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="libelle", type="string", length=50, unique=true)
     */
    private $libelle;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set libelle
     *
     * @param string $libelle
     *
     * @return MethodePaiement
     */
    public function setLibelle($libelle)
    {
        $this->libelle = $libelle;

        return $this;
    }

    /**
     * Get libelle
     *
     * @return string
     */
    public function getLibelle()
    {
        return $this->libelle;
    }
}

